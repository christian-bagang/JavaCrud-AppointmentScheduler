    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name = anf.getText();
            String info = aif.getText();
            String dateandtime = datf.getText();
            String location = lf.getText();
            String status = "Pending";
            
            pst = con.prepareStatement("INSERT INTO appointments (name,info,dateandtime,location,status) VALUES (?,?,?,?,?)");
            pst.setString(1, name);
            pst.setString(2, info);
            pst.setString(3,dateandtime);
            pst.setString(4,location);
            pst.setString(5,status);
            pst.executeUpdate();
            
            anf.setText("");
            aif.setText("");
            datf.setText("");
            lf.setText("");
            
            RenderAppointmentTable();
        } catch (SQLException ex) {
            Logger.getLogger(ASForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int[] x = jTable1.getSelectedRows(); 
        for (int i = 0 ; i < x.length ; i++){
            try {
                Object idO = df.getValueAt(x[i],0); // getValueAt(0,0);
                pst = con.prepareStatement("DELETE FROM appointments WHERE id = ?");
                pst.setString(1, idO.toString());
                pst.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(ASForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
       RenderAppointmentTable();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String status = (String) combobox.getSelectedItem();
        int[] x = jTable1.getSelectedRows();
        for (int i = 0 ; i < x.length ; i++){
            try {
                Object selectedRow = df.getValueAt(x[i],0);
                pst = con.prepareStatement("UPDATE appointments SET status = ? WHERE id = ?");
                pst.setString(1, status);                
                pst.setString(2, selectedRow.toString());
                pst.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(ASForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        RenderAppointmentTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
