        public void RenderAppointmentTable(){
        try {
            
            int q;
            pst = con.prepareStatement("SELECT * FROM appointments");
            rs = pst.executeQuery();
            
            ResultSetMetaData rss = rs.getMetaData();
            q = rss.getColumnCount();
     
            df =(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for (int i = 1 ; i <= q ; i++){
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("info"));
                    v2.add(rs.getString("dateandtime"));
                    v2.add(rs.getString("location"));
                    v2.add(rs.getString("status"));
                }
                df.addRow(v2);
            } 
            } catch (SQLException ex) {
            Logger.getLogger(ASForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

